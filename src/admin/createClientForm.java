         if (pangan.getText().isEmpty() || apilido.getText().isEmpty()
    || lugar.getText().isEmpty() || dateField.getText().isEmpty()
    || oras.getText().isEmpty() || contact1.getText().isEmpty()) {

    JOptionPane.showMessageDialog(null, "Please fill in all fields.");

} else {
    dbConnector dbc = new dbConnector();

    try {
        String sql = "UPDATE client_book SET c_fname = ?, c_lname = ?, c_event = ?, c_date = ?, "
                   + "c_time = ?, c_place = ?, c_payment = ?, c_contact = ? WHERE c_id = ?";

        PreparedStatement pst = dbc.connect.prepareStatement(sql);
        pst.setString(1, pangan.getText());
        pst.setString(2, apilido.getText());
        pst.setString(3, klasiha.getSelectedItem().toString());
        pst.setString(4, dateField.getText());
        pst.setString(5, oras.getText());
        pst.setString(6, lugar.getText());
        pst.setString(7, bayad.getText());
        pst.setString(8, contact1.getText());
        pst.setString(9, clientID.getText());

        int updated = pst.executeUpdate();

        if (updated > 0) {
            JOptionPane.showMessageDialog(null, "Client updated successfully!");
        } else {
            JOptionPane.showMessageDialog(null, "Update failed. Record may not exist.");
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Connection Error: " + e.getMessage());
    }

    clientsForm cf = new clientsForm();
    cf.setVisible(true);
    this.dispose();
}

