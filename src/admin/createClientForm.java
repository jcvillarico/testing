    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed

       if (pangan.getText().isEmpty() || apilido.getText().isEmpty()
        || lugar.getText().isEmpty() || dateField.getText().isEmpty()
        || oras.getText().isEmpty() || contact1.getText().isEmpty() || bayad.getText().isEmpty()) {

    JOptionPane.showMessageDialog(null, "All fields are required!");

} else if (!contact1.getText().matches("\\d{11}")) {
    JOptionPane.showMessageDialog(null, "Contact number must contain only digits and be 11 digits long.");
    contact1.setText("");

} else {
    dbConnector dbc = new dbConnector();

    String sql = "INSERT INTO client_book (c_fname, c_lname, c_event, c_date, c_time, c_place, c_payment, c_contact) "
               + "VALUES (?, ?, ?, ?, ?, ?, ?, ?)";

    try {
        PreparedStatement pst = dbc.connect.prepareStatement(sql);
        pst.setString(1, pangan.getText());
        pst.setString(2, apilido.getText());
        pst.setString(3, klasiha.getSelectedItem().toString());
        pst.setString(4, dateField.getText());
        pst.setString(5, oras.getText());
        pst.setString(6, lugar.getText());
        pst.setString(7, bayad.getText());
        pst.setString(8, contact1.getText());

        int inserted = pst.executeUpdate();

        if (inserted > 0) {
            JOptionPane.showMessageDialog(null, "Booking added successfully!");
           clientsForm cf = new clientsForm();
           cf.setVisible(true);
           this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Insert failed.");
        }

    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage());
    }
}

    

    }//GEN-LAST:event_addActionPerformed

